#include "3-calc.h"

/**
 * op_add - to add two ints
 * @a: first int
 * @b: 2nd int
 * Return: sum of a, b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - to subtract two ints
 * @a: first int
 * @b: 2nd int
 * Return: difference of a, b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - to multiply two ints
 * @a: first int
 * @b: 2nd int
 * Return: product of a, b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - to divide two ints
 * @a: first int
 * @b: 2nd int
 * Return: division of a, b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - to remainder after division of two ints
 * @a: first int
 * @b: 2nd int
 * Return: modulus of a, b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
